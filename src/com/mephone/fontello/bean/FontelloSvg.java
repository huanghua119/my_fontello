package com.mephone.fontello.bean;

public class FontelloSvg {

    private String fontFamily;

    private String fontWeight;

    private String fontStretch;

    private String unitsPerEm;

    private String ascent;

    private String descent;

    private String glyph;

    public String getFontFamily() {
        return fontFamily;
    }

    public void setFontFamily(String fontFamily) {
        this.fontFamily = fontFamily;
    }

    public String getFontWeight() {
        return fontWeight;
    }

    public void setFontWeight(String fontWeight) {
        this.fontWeight = fontWeight;
    }

    public String getFontStretch() {
        return fontStretch;
    }

    public void setFontStretch(String fontStretch) {
        this.fontStretch = fontStretch;
    }

    public String getUnitsPerEm() {
        return unitsPerEm;
    }

    public void setUnitsPerEm(String unitsPerEm) {
        this.unitsPerEm = unitsPerEm;
    }

    public String getAscent() {
        return ascent;
    }

    public void setAscent(String ascent) {
        this.ascent = ascent;
    }

    public String getDescent() {
        return descent;
    }

    public void setDescent(String descent) {
        this.descent = descent;
    }

    public String getGlyph() {
        return glyph;
    }

    public void setGlyph(String glyph) {
        this.glyph = glyph;
    }

}
