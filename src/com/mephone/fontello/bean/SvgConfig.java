package com.mephone.fontello.bean;

public class SvgConfig {

    private String unitsPerEm;

    private String ascent;

    private String name;
    
    private String svgDir;

    public String getUnitsPerEm() {
        return unitsPerEm;
    }

    public void setUnitsPerEm(String unitsPerEm) {
        this.unitsPerEm = unitsPerEm;
    }

    public String getAscent() {
        return ascent;
    }

    public void setAscent(String ascent) {
        this.ascent = ascent;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSvgDir() {
        return svgDir;
    }

    public void setSvgDir(String svgDir) {
        this.svgDir = svgDir;
    }

}
